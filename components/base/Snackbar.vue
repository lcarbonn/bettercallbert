<template>
    <div ref="snackbar"
         class="snackbar">{{ label }}</div>
</template>

<script>
export default {
    props: {
        label: {
            type: String,
            required: true
        }
    },
    methods: {
        show() {
            const snackbar = this.$refs.snackbar;
            snackbar.classList.add('is__visible');
            setTimeout(() => {
                snackbar.classList.remove('is__visible');
            }, 3000);
        }
    }
};
</script>

